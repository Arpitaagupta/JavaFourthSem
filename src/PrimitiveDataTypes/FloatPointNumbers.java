package PrimitiveDataTypes;

public class FloatPointNumbers {
    public static void main(String[] args) {
        float ff = 20.4f;
        double d =ff;
        long l = 204;
        System.out.println("float f = "+ff+" and double d = "+d);

        int x = (int)ff;
        System.out.println("x = "+x);

        double dd = (double)l;
        System.out.println("dd = "+dd);
    }
}
